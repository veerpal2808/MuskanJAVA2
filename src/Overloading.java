
public class Overloading {

	static void qwe() {
		System.out.println("Muskan is a Vampire");
	}
		
		static void qwe(int a) {
			System.out.println("Muskan is an" +a+" original Vampire");
		
		}
		
		static void qwe(int a, int b) {
			System.out.println("Muskan is a " +a+"warewolf ");
			System.out.println("Muskan is a " +b+"warewolf ");
		}
	

  public static void main (String []  args) {
	  
	  qwe();
	  qwe(275);
	  qwe(24,54)
;	  
	  
}}
